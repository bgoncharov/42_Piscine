int		ft_atoi(char *str)
{
	int res;
	int sign;

	res = 0;
	sign = 1;
	while (*str == '\n' || *str == ' ' || *str == '\t' || *str == '\r' ||
		*str == '\v' || *str == '\f')
		++str;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign = 0;
		str++;
	}
	while ((*str) >= '0' && (*str) <= '9')
	{
		if (sign)
			res = (res * 10) + (*str - '0');
		else
			res = (res * 10) - (*str - '0');
		str++;
	}
	return (res);
}
