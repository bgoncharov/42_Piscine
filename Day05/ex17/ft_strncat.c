char	*ft_strncat(char *dest, char *src, int nb)
{
	int z;
	int j;

	z = 0;
	j = 0;
	while (dest[j])
		j++;
	while (z < nb)
	{
		dest[j + z] = src[z];
		z++;
	}
	dest[j + z] = '\0';
	return (dest);
}
