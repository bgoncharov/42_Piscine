int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int z;

	z = 0;
	while (z < n && s1[z] != '\0' && s2[z] != '\0')
	{
		if (s1[z] != s2[z])
			return (s1[z] - s2[z]);
		++z;
	}
	if (z != n)
		return (s1[z] - s2[z]);
	return (0);
}
