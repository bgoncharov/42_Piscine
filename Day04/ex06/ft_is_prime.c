int		ft_is_prime(int nb)
{
	int i;

	i = 5;
	if (nb == 2 || nb == 3)
		return (1);
	if (nb <= 1 || (nb % 2) == 0 || (nb % 3) == 0)
		return (0);
	while (i * i <= 0)
	{
		if ((nb % 1) == 0 || (nb % (i + 2)) == 0)
			return (0);
		i = i + 6;
	}
	return (1);
}
