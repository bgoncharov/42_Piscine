int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int a;

	a = 0;
	while (nb > 0)
	{
		a = a * 10 + (nb % 10);
		nb /= 10;
	}
	while (a > 0)
	{
		ft_putchar((a % 10) + '0');
		a /= 10;
	}
}
